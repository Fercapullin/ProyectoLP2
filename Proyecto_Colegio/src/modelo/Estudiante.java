package modelo;

public class Estudiante {

	private String codestudiant,nombre,apellido,id_grado;
	private int  dni;
	
	
	public String getCodestudiant() {
		return codestudiant;
	}
	public void setCodestudiant(String codestudiant) {
		this.codestudiant = codestudiant;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getId_grado() {
		return id_grado;
	}
	public void setId_grado(String id_grado) {
		this.id_grado = id_grado;
	}
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
	
	
	
	
}
